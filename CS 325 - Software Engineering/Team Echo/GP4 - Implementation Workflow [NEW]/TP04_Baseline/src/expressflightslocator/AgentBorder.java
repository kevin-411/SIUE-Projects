    private void addTravelerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTravelerButtonActionPerformed
        if(getNamesOfTravelersField().getText() == null){
            JOptionPane.showMessageDialog(null, "Please Enter A Valid Name Into The Text Field And Click Add!!", "Failed Name Addition To List!!", JOptionPane.ERROR_MESSAGE);
        }else if("5".equals(getNumberOfTravelersField().getText())){
           JOptionPane.showMessageDialog(null, "You can not have more than 5 travelers on one itinerary!!", "Failed Name Addition To List!!", JOptionPane.ERROR_MESSAGE);
        }else{
            getNamesOfTravelersList().add(getNamesOfTravelersField().getText());
            getNumberOfTravelersField().setText(String.valueOf(Integer.parseInt(getNumberOfTravelersField().getText()) + 1));
            getNamesOfTravelersField().setText(null);
        }
    }//GEN-LAST:event_addTravelerButtonActionPerformed

    private void deleteTravelersButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTravelersButtonActionPerformed
        if(getNamesOfTravelersList().getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Please Select A Name From The List And Click Delete!!", "Failed Name Deletion To List!!", JOptionPane.ERROR_MESSAGE);
        }else if("0".equals(getNumberOfTravelersField().getText())){
           JOptionPane.showMessageDialog(null, "You can not have less than 0 travelers on the itinerary!!", "Failed Name Deletion To List!!", JOptionPane.ERROR_MESSAGE);
        }else{
            getNamesOfTravelersList().remove(getNamesOfTravelersList().getSelectedIndex());
            getNumberOfTravelersField().setText(String.valueOf(Integer.parseInt(getNumberOfTravelersField().getText()) - 1));
        }
    }//GEN-LAST:event_deleteTravelersButtonActionPerformed

